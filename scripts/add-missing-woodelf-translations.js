const languagesToUpdate = [
  'ja', 'ko', 'ar', 'id', 'it', 'pl', 'pt', 'ru', 'th', 'vi', 'es', 'fr'
];

function addWoodElfSeoTranslations() {
  languagesToUpdate.forEach(lang => {
    const filePath = path.join(__dirname, '..', 'messages', `${lang}.json`);
    
    try {
      // 读取现有的翻译文件
      const fileContent = fs.readFileSync(filePath, 'utf8');
      const translations = JSON.parse(fileContent);
      
      // 检查是否已经有 woodElfSeo 部分
      if (!translations.woodElfSeo) {
        console.log(`Adding woodElfSeo translations to ${lang}.json`);
        
        // 添加 woodElfSeo 翻译
        translations.woodElfSeo = woodElfSeoTemplate;
        
        // 写回文件
        fs.writeFileSync(filePath, JSON.stringify(translations, null, 2), 'utf8');
        console.log(`✅ Successfully added woodElfSeo translations to ${lang}.json`);
      } else {
        console.log(`⏭️  ${lang}.json already has woodElfSeo translations`);
      }
    } catch (error) {
      console.error(`❌ Error processing ${lang}.json:`, error.message);
    }
  });
}

// 运行脚本
